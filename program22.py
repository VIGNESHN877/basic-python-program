"""
write a python Program to find LCM.

LCM OR Least Common Multiple, is the smallest number that is a multiple of two or more numbers.

formula:

for two numbers a and b, the LCM can be calculated using the following formula:

LCM(a, b) = (a * b) / GCD(a, b)

for more than two numbers, the LCM can be calculated iteratively.

"""

limit=int(input("Enter the limit: "))

# Initialize the Sum

sum=0

#Use a for Loop to calculate the sum of the natural numbers

for i in range(1,limit+1):
    sum +=i

print("The sum of Natural numbers up to ",limit,"is :",sum)

